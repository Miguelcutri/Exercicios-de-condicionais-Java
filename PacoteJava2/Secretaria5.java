//5) A Secretaria de Meio Ambiente que controla o índice de poluição mantém 3 grupos
//de indústrias que são altamente poluentes do meio ambiente. O índice de poluição aceitável
//varia de 0,05 até 0,25. Se o índice sobe para 0,3 as indústrias do 1º grupo são intimadas a
//suspenderem suas atividades, se o índice crescer para 0,4 as industrias do 1º e 2º grupo são
//intimadas a suspenderem suas atividades, se o índice atingir 0,5 todos os grupos devem ser
//notificados a paralisarem suas atividades. Faça um sistema que leia o índice de poluição medido
//e emita a notificação adequada aos diferentes grupos de empresas.


package PacoteJava2;
import java.util.Scanner;
public class Secretaria5 {
	public static void main(String[] args) {
		float indice;         
		Scanner ler = new Scanner(System.in);         
		System.out.println("Qual o índice de poluição?: ");         
		indice = Float.parseFloat(ler.nextLine());    
		System.out.println(indice); 
		if (indice >= 0.5 ) {             
			System.out.println("Todas as industrias devem suspenderem suas atividades.");         
			} 
		else if (indice >= 0.05 && indice<= 0.25) {             
			System.out.println("Índice aceitável");         
			}         
		else if (indice >= 0.3 ) {             
			System.out.println("Industria 1 - Suspensa de atividades.");         
			}                  
		else if (indice >= 0.4 ) {             
				System.out.println("Industria 1 e 2 - Suspensa de atividades.");         
				}         
      
		   
		}  
	}